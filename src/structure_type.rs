macro_rules! impl_structure_type {
    ($($x:literal = $structure_name:ident),*) => {
        /// Structure type for Vulkan Structs
        /// Created By Macro
        #[derive(Clone, Copy, Debug, PartialEq)]
        pub enum StructureType {
            $(
                $structure_name = $x,
            )*
        }

        impl From<i32> for StructureType {
            fn from(x: i32) -> Self {
                match x {
                    $($x => StructureType::$structure_name,)*
                    _ => panic!("Invalid StructureType {}", x),
                }
            }
        }
    }
}

